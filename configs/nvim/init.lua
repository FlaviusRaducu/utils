-- Enable line numbers
vim.opt.number = true

-- Set tabstop and shiftwidth
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4

-- Set folding method to indent
vim.opt.foldmethod = "indent"

-- Enable spell checking
vim.opt.spell = true
vim.opt.spelllang = "en_gb"

-- Set colorscheme
vim.cmd("colorscheme slate")
